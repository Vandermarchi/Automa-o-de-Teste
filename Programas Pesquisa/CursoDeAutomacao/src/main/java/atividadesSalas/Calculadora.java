package atividadesSalas;

public class Calculadora {

	// soma dvidir subtrair multiplicar
	// voce deve passar 2 numeros como variavei de atributo ou seja dentro do (int
	// num1 ,int num2)
	// metodo com retorno inteiro

	public int soma(int num1, int num2) {

		return num1 + num2;
	}

	public int subtracao(int num1, int num2) {

		return num1 - num2;
	}

	public int divisao(int num1, int num2) {

		return num1 / num2;
	}

	public int multiplicacao(int num1, int num2) {

		return num1 * num2;
	}

}
