package br.com.e2etreinamentos.automacao;

public class SwitchCase {

	public static void main(String[] args) {
		
		String browser = "FIREFOX";
		
		switch (browser) {
		
		case "CHROME":
		
		System.out.println(" Você escolheu o navegador "+ browser);
		
		break;
		
		case "FIREFOX":
			
			System.out.println(" Você escolheu o navegador "+ browser);
			
			break;
		}

	}

}
